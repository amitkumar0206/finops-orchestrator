import asyncio

from backend.services.iac_analysis_service import IacAnalysisService


TERRAFORM_SAMPLE = """
resource \"aws_instance\" \"demo\" {
  ami           = \"ami-123\"
  instance_type = \"m5.large\"
}

resource \"aws_db_instance\" \"db\" {
  identifier  = \"demo-dev-db\"
  multi_az    = true
}

resource \"aws_ebs_volume\" \"data\" {
  volume_type = \"gp2\"
}
"""


CLOUDFORMATION_SAMPLE = """
AWSTemplateFormatVersion: '2010-09-09'
Resources:
  App:
    Type: AWS::EC2::Instance
    Properties:
      InstanceType: m5.xlarge
  Db:
    Type: AWS::RDS::DBInstance
    Properties:
      MultiAZ: true
"""


def test_analyze_terraform_generates_signals():
    svc = IacAnalysisService()

    record = asyncio.run(
        svc.analyze_file(
            filename="demo.tf",
            content=TERRAFORM_SAMPLE,
            owner_user_id="u1",
            owner_org_id="o1",
        )
    )

    assert record.format == "terraform"
    assert record.analysis_id
    assert any("gp2" in c.lower() for c in record.cons)
    assert any(row["estimated_monthly_cost_usd"] > 0 for row in record.cost_analysis)
    assert "Generated by FinOps Orchestrator" in record.improved_content


def test_analyze_cloudformation_detects_multi_az():
    svc = IacAnalysisService()

    record = asyncio.run(
        svc.analyze_file(
            filename="stack.yaml",
            content=CLOUDFORMATION_SAMPLE,
            owner_user_id="u1",
            owner_org_id="o1",
        )
    )

    assert record.format == "cloudformation"
    assert any("multiaz" in c.lower().replace("-", "") or "multiaz" in c.lower().replace(" ", "") for c in record.cons)


