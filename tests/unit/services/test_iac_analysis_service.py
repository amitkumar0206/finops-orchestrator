def test_owner_check_blocks_other_user():
    svc = IacAnalysisService()

    record = asyncio.run(
        svc.analyze_file(
            filename="demo.tf",
            content=TERRAFORM_SAMPLE,
            owner_user_id="owner-user",
            owner_org_id="org-1",
        )
    )

    try:
        asyncio.run(
            svc.chat_about_template(
                analysis_id=record.analysis_id,
                question="Can I remove Multi-AZ?",
                user_id="different-user",
                org_id="org-1",
            )
        )
        assert False, "Expected PermissionError"
    except PermissionError:
        assert True


def test_analyze_files_returns_cross_file_payload():
    svc = IacAnalysisService()
    files = [
        ("network.tf", 'resource "aws_instance" "web" {\n  instance_type = "m5.xlarge"\n}\n'),
        ("compute.tf", 'resource "aws_instance" "web" {\n  instance_type = "m5.large"\n}\n'),
    ]

    result = asyncio.run(svc.analyze_files(files=files, owner_user_id="u2", owner_org_id="o1"))

    assert result["file_count"] == 2
    assert len(result["records"]) == 2
    assert result["primary"].filename == "network.tf"
    assert isinstance(result["cross_file_analysis"]["architecture_observations"], list)
    assert isinstance(result["cross_file_analysis"]["recommendations"], list)
    assert result["cross_file_analysis"]["total_estimated_monthly_savings"] >= 0


def test_cross_file_analysis_detects_duplicates_and_regions():
    svc = IacAnalysisService()
    files = [
        ("main.tf", 'provider "aws" { region = "us-east-1" }\nresource "aws_instance" "api" {\n  instance_type = "m5.xlarge"\n}\n'),
        ("secondary.tf", 'provider "aws" { region = "us-west-2" }\nresource "aws_instance" "api" {\n  instance_type = "m5.large"\n}\n'),
    ]

    result = asyncio.run(svc.analyze_files(files=files, owner_user_id="u3", owner_org_id="o1"))
    cross = result["cross_file_analysis"]

    duplicate_details = "\n".join(cross.get("duplicate_resource_details", []))
    assert "aws_instance.api" in duplicate_details
    assert set(cross.get("regions_detected", [])) == {"us-east-1", "us-west-2"}
